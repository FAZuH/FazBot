from __future__ import annotations
from contextlib import contextmanager
from typing import Generator, Protocol, TYPE_CHECKING

if TYPE_CHECKING:
    from fazbot import Asset, Bot, Config, Heartbeat, Logger


class Core(Protocol):
    """<<interface>>"""
    def start(self) -> None: ...
    def stop(self) -> None: ...
    @contextmanager
    def get_asset_synced(self) -> Generator[Asset]: ...
    @contextmanager
    def get_bot_synced(self) -> Generator[Bot]: ...
    @contextmanager
    def get_config_synced(self) -> Generator[Config]: ... 
    @contextmanager
    def get_heartbeat_synced(self) -> Generator[Heartbeat]: ...
    @contextmanager
    def get_logger_synced(self) -> Generator[Logger]: ...

